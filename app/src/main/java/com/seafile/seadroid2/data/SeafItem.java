package com.seafile.seadroid2.data;

/**
 * The interface Seaf item.
 */
public interface SeafItem {
    /**
     * Gets title.
     *
     * @return the title
     */
    String getTitle();

    /**
     * Gets subtitle.
     *
     * @return the subtitle
     */
    String getSubtitle();

    /**
     * Gets icon.
     *
     * @return the icon
     */
    int getIcon();
}
